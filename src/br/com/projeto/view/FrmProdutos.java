    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        try{
            Produtos obj = new Produtos();
            obj.setDescricao(descricao.getText());
            obj.setPreco(Double.parseDouble(preco.getText()));
            obj.setQtd_estoque(Integer.parseInt(qtdestoque.getText()));
            
            Fornecedores f = new Fornecedores();
            f = (Fornecedores) cdFornecedor.getSelectedItem();
            obj.setFornecedor(f);            
            ProdutosDAO dao = new ProdutosDAO();            
            dao.cadastrar(obj);
            
            new Utilitarios().limpaTela(painelDeDados);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro" + e );
        }
    }//GEN-LAST:event_btnsalvarActionPerformed

    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        try{
            Produtos obj = new Produtos();
            obj.setId(Integer.parseInt(codigo.getText()));
            obj.setDescricao(descricao.getText());
            obj.setPreco(Double.parseDouble(preco.getText()));
            obj.setQtd_estoque(Integer.parseInt(qtdestoque.getText()));    
            
            Fornecedores f = new Fornecedores();
            f=(Fornecedores) cdFornecedor.getSelectedItem();
            obj.setFornecedor(f);

            ProdutosDAO dao = new ProdutosDAO();            
            dao.alterar(obj);
            
            new Utilitarios().limpaTela(painelDeDados);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro" + e );
        }
    }//GEN-LAST:event_btneditarActionPerformed

