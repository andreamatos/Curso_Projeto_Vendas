    private void btnbuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscaActionPerformed
            
            String nome = this.descricao.getText();
            
            Produtos obj = new Produtos();
            ProdutosDAO dao = new ProdutosDAO();
            
            obj = dao.consultaPorNome(nome);
            cdFornecedor.removeAllItems();
            
            if(obj.getDescricao() != null){
                codigo.setText(String.valueOf(obj.getId()));
                descricao.setText(obj.getDescricao());
                preco.setText(String.valueOf(obj.getPreco()));
                qtdestoque.setText(String.valueOf(obj.getQtd_estoque()));

                Fornecedores f = new Fornecedores();
                FornecedoresDAO fdao = new FornecedoresDAO();
                
                f = fdao.consultaPorNome(obj.getFornecedor().getNome());
                
                cdFornecedor.getModel().setSelectedItem(f);
               
            }else{
                JOptionPane.showMessageDialog(null, "Produto não encontrado");
            }
            
    }//GEN-LAST:event_btnbuscaActionPerformed

